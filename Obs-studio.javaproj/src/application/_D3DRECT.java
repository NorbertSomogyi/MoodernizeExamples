package application;

public class _D3DRECT {
	private Object x1;
	private Object y1;
	private Object x2;
	private Object y2;
	
	public _D3DRECT(Object x1, Object y1, Object x2, Object y2) {
		setX1(x1);
		setY1(y1);
		setX2(x2);
		setY2(y2);
	}
	public _D3DRECT() {
	}
	
	public Object getX1() {
		return x1;
	}
	public void setX1(Object newX1) {
		x1 = newX1;
	}
	public Object getY1() {
		return y1;
	}
	public void setY1(Object newY1) {
		y1 = newY1;
	}
	public Object getX2() {
		return x2;
	}
	public void setX2(Object newX2) {
		x2 = newX2;
	}
	public Object getY2() {
		return y2;
	}
	public void setY2(Object newY2) {
		y2 = newY2;
	}
}
