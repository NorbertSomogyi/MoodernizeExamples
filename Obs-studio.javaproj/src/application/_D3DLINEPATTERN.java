package application;

public class _D3DLINEPATTERN {
	private Object wRepeatFactor;
	private Object wLinePattern;
	
	public _D3DLINEPATTERN(Object wRepeatFactor, Object wLinePattern) {
		setWRepeatFactor(wRepeatFactor);
		setWLinePattern(wLinePattern);
	}
	public _D3DLINEPATTERN() {
	}
	
	public Object getWRepeatFactor() {
		return wRepeatFactor;
	}
	public void setWRepeatFactor(Object newWRepeatFactor) {
		wRepeatFactor = newWRepeatFactor;
	}
	public Object getWLinePattern() {
		return wLinePattern;
	}
	public void setWLinePattern(Object newWLinePattern) {
		wLinePattern = newWLinePattern;
	}
}
