package application;

public class in_pack_object {
	private Object offset;
	private object object;
	
	public in_pack_object(Object offset, object object) {
		setOffset(offset);
		setObject(object);
	}
	public in_pack_object() {
	}
	
	public Object getOffset() {
		return offset;
	}
	public void setOffset(Object newOffset) {
		offset = newOffset;
	}
	public object getObject() {
		return object;
	}
	public void setObject(object newObject) {
		object = newObject;
	}
}
